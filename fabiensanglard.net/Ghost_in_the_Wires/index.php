			<h3>One: Rough Start</h3><br/>
			<cite>yjcv ku vjg pcog qh vjg uauvgo wugf da jco qrgtcvqtu vq ocmg htgg rjqpg ecnnu ?</cite><br/><br/>
			<p id="paperbox">
			Looks a lot like words. And it is the first chapter so it is probably the easiest and most known cypher
			off all time: ROT-2

<pre  class="long">
					

            yjcv ku vjg pcog qh vjg uauvgo wugf da jco qrgtcvqtu vq ocmg htgg rjqpg ecnnu ?
            what is the name of the system used by ham operators to make free phone calls ?
			
			
			</pre> 
			<p>
			<u>Note :</u> There is a good tool online that also provide frequency analysis: 
			<a href="http://web.forret.com/tools/rot13.asp?">ROT-13 encrypt/decrypt (Caesar cipher)</a>. The frequency 
			distribution is consistent with a ROT encryption:<br/>
			<br/>
			<u>Edit :</u> I wrote a quick C program called CrackKevin.exe to automate ROT testing. <a href="main.c.txt">Here it is</a>. It works by testing all 26 ROT and then searching
				for keyword in the deciphered string. The <a href="output.txt">output</a> helped to identify very fast a ROT candidate and solved 
				17 out of the 38 encrypted strings ;) !<br/>
