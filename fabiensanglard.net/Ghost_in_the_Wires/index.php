   <h1>Solving Kevin Mitnick's Ghost in The Wires encrypted messages.</h1>
   <p id="paperbox">
	   	
          <a  href="index.php">
          <img src="icon.jpg" style="margin-left: 2ch;float:right; border:1px black solid;width:35%;">   
