				I received today my copy of Kevin Mitnick's book: 
				<a href="http://www.amazon.com/Ghost-Wires-Adventures-Worlds-Wanted/dp/0316037702">Ghost in The Wires</a>.
				It is a great read so far. But even more interesting are the ciphered sentences at the beginning of 
				each chapters. I am trying to solve all of them but if you can contribute: comment or send me an email !<br/>
				<br/>
				<br/>
				<u>EDIT (September 11, 2011 1:04am):</u> All codes have been broken, this page is the complete solution to "Ghost in The Wires".
				<br/>
				<br/>
				<u>Post-mortem :</u> Most of the cryptograms were simple constant ROT and the program I wrote to solve them proved very efficient. The program was inspired by an excellent book by Simon Singh: 
				<a href="http://www.amazon.com/Code-Book-Science-Secrecy-Cryptography/dp/0385495323/ref=sr_1_1?ie=UTF8&qid=1316405428&sr=8-1">The Code Book: 
				The Science of Secrecy from Ancient Egypt to Quantum Cryptography"</a>. In this book Singh described how Alan Turing exploited the <i>cribs</i> 
				in Nazy Enigma encrypted messages by looking for the probable location of the german word "WETTER" ("weather" in english). The "colossus" 
				could only test 5 letters in order to find the key of the day but nowadays the raw power allowed me to test all possible rotations searching for 
				cribs in Mitnik sentences (since most seemed to be "questions": the program automatically searched for "WHAT","WHY,"WHERE" and others words usually found at the beginning of a sentence). 
				This method allowed to decrypt 50% of the messages within 30 seconds...and since the other 50% can be decrypted using the answer from the previous question:
				 Almost all cryptograms were decoded thanks to Turin's work to break Enigma....60 years ago. </a>
