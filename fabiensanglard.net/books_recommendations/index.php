<br/>I have gathered here the few books I consider to be masterpiece about computer stacks. There are two for each levels of the stacks:<br/>
<ul>
    <li>Hardware.</li>
    <li>Operating Systems.</li>
    <li>Programming Language.</li>
    
</ul>
<p>And since the lack of enthousiam is the root of mediocrity I have also added a category "Inspiration" .
<div style="clear:both"/>
</p>
              


<br/>      
<h2>Hardware</h2>
<p id="paperbox">
<img src="code.png"  class="shadowed-book" style="float:left;"/><a href="http://www.amazon.com/Code-Language-Computer-Hardware-Software/dp/0735611319/ref=sr_1_1?ie=UTF8&qid=1358293457&sr=8-1&keywords=code+hidden+language">Code: The Hidden Language of Computer Hardware and Software</a><br/>
    Charles Petzold's masterpiece explains how hardware works to the deep down. Starting from scratch, with countless drawings, information flows from the pages to your brain without effort.
Explaining like if you were 5 years old, it follows perfectly the book quality rule: "Quality is inversely proportional to its size".
<div style="clear:both"/>
<br/>
<p id="paperbox">
<img src="computer_org_design.png" class="shadowed-book" style="float:left;"/><a href="http://www.amazon.com/Computer-Organization-Design-Fourth-Architecture/dp/0123747503/ref=sr_1_1?s=books&ie=UTF8&qid=1358293499&sr=1-1&keywords=computer+organization+and+design">Computer Organization and Design</a><br/>
Excellent description of modern CPU internals and how to reach Software/Hardware synergy. Prefetching, cacheline or Out-Of-Order execution (to name only a few) are clearly explained. If the "Code" book is the sparkle, "Organization and Design" is the explosion.
<div style="clear:both"/>
<br/>
<p id="paperbox">
<br/>
<b><u>Trivia :</u></b> An example of speed improvement due to good hardware knowledge: Doom stored textures 
horizontally in RAM to take full advantage of the cacheline.<br/>
<br/>
<b><u>Trivia :</u></b> An other example of speed improvement due to good hardware knowledge: OpenGL Interleaved 
arrays are faster than separate arrays (thanks to cacheline again).
</p>






<br/>
<h2>Operating Systems</h2>
<p id="paperbox">
Whether it is to perform I/O, display something on the screen or communicate on the network you program will have to leave user-space at some point. What happens in kernel-space is explained here.<br/>
<br/>
<img src="op_systems.png" class="shadowed-book" style="float:left;"/><a href="http://www.amazon.com/Modern-Operating-Systems-3rd-Edition/dp/0136006639/ref=pd_sim_b_1">Modern Operating Systems </a><br/>
Virtual Time, Virtual Memory, Virtual Filesystem, System calls, Sleep/Busy Locks and Process scheduler (to name only a few key concepts) are clearly explained in this great book by Tanenbaum.
<div style="clear:both"/>
<br/>
<p id="paperbox">
<img src="linux_prog_intef.png" class="shadowed-book" style="float:left;"/><a href="http://www.amazon.com/Linux-Programming-Interface-System-Handbook/dp/1593272200/ref=sr_1_1?s=books&ie=UTF8&qid=1358291843&sr=1-1&keywords=the+linux+programming+interface">The Linux Programming Interface</a><br/>
Have you ever wondered how <code>malloc</code> is implemented by the C Library ? This book offers a top-down approach to understand what the Operating System can do for your application/library. A departure from the bottom-up description of "Operating Systems: Design and Implementation" that may be easier for some readers. A must have regardless.
<div style="clear:both"/>

<p id="paperbox">
<br/>
<b><u>Trivia :</u></b> An example of speed improvement due to good knowledge of page-fault: <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=627591">Firefox boots two seconds faster thanks to DLL preloading</a>.
<p id="paperbox">
</p>






