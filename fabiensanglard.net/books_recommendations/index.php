<h2>Inspiration</h2>
<p id="paperbox">
Three components lead to success:
<ul><li>Passion.</li><li>Skills</li><li>Luck.</li></ul>
<p>Most of us have Passion and Skills. It is very inspiring to 
see what happen when Luck is also part of the equation:<br/>
<br/>

<img src="pixar_touch.png" class="shadowed-book" style="float:left;"/>
  <a href="http://www.amazon.com/Pixar-Touch-Vintage-David-Price/dp/0307278298/ref=sr_1_2?ie=UTF8&qid=1358291346&sr=8-2">The Pixar Touch</a>:<br/> 
They should have gone broke and failed several times...but instead flew to Infinity and Beyond ! Many delicious technical trivias 
(early days of Framebuffers, texture mapping) and great anecdotes about human rivalties/survival: Passion, Skills and a LOT of Luck.  
Also: Crazy schedules.
<div style="clear:both"/>
<br/>
<p id="paperbox">
<img src="masters_doom.png" class="shadowed-book" style="float:left;"/>
  <a href="http://www.amazon.com/Masters-Doom-Created-Transformed-Culture/dp/0812972155/ref=sr_1_1?s=books&ie=UTF8&qid=1358291377&sr=1-1">Masters of Doom</a>:<br/> 
  Passion, Binary Space Partition and a little bit of Luck ! Also: Crazy schedules.
<div style="clear:both"/>

</p>   



<br/>
<h2>Adapting the stack</h2>
<p id="paperbox">
The books mentioned should satisfy most stacks but it is paramount to adapt it to your specific needs. Since I do a fair amount of network programming those times, I would certainly include
the masterpiece: "<a href="http://www.amazon.com/TCP-Guide-Comprehensive-Illustrated-Protocols/dp/159327047X/ref=sr_1_6?s=books&ie=UTF8&qid=1358377001&sr=1-6">The TCP/IP Guide</a>".<br/>
<br/>My past career as a 3D-engine developer involved a lot of Computer Graphics, I would have mentioned the following:<br/>
<br/>
<img src="realtime_rendering.png" class="shadowed-book" style="float:left;"/><a href="http://www.amazon.com/Real-Time-Rendering-Third-Tomas-Akenine-Moller/dp/1568814240/ref=sr_1_1?s=books&ie=UTF8&qid=1358291525&sr=1-1&keywords=real+time+rendering">Real-Time Rendering</a><br/>
An extensive reference that describes the recent realtime rendering techniques. An essential map that will help you to know where you want to dig deeper. A CG Classic.
<div style="clear:both"/>
<br/>
<p id="paperbox">
<img src="abrash_black_book.png" class="shadowed-book" style="float:left;"/><a href="http://www.amazon.com/Michael-Abrashs-Graphics-Programming-Special/dp/1576101746/ref=sr_1_1?s=books&ie=UTF8&qid=1358291481&sr=1-1&keywords=michael+abrash">Michael Abrash's Graphics Programming Black Book</a><br/>
A very old book that still contains very good lessons (especially planes mathematics) but really an inspiring celebration of Michael Abrash's motto: "Do what you Love, work with humility and Good Things will come eventually".
<div style="clear:both"/>
  </p>        


