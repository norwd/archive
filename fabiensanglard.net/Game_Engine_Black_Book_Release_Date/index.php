 <p>
  How was Wolfenstein 3D made and what were the secrets of its speed?
   How did id Software manage to turn a machine designed to display static images for
    word processing and spreadsheet applications into the best gaming platform in the world,
     capable of running games  at seventy frames per seconds? If you have ever asked yourself these questions, 
     <i>Game Engine Black Book</i> is for you.<br/>
<br/>

This is an engineering book. You will not find much prose in it (the authorâ€™s English is broken anyway.) 
Instead, this book has only bit of text and plenty of drawings attempting to describe in great detail 
the Wolfenstein 3D game engine and its hardware, the IBM PC with an Intel 386 CPU and a VGA graphic card.<br/>
<br/>
</p>
<div style="clear:both;"></div>

<h1>Content</h1>
<p id="paperbox">
<i>Game Engine Black Book</i> details techniques such as raycasting, compiled scalers, deferred rendition, VGA Mode-Y,
 linear feedback shift register, fixed point arithmetic, pulse width modulation, runtime generated code, 
 self-modifying code, and many others tricks.<br/>
 <br/>
 It also describes extensively the hardware of 1991. The architecture of the Intel 386, 
 the limitations of DOS mandated real mode, the VGA, Input programming, and the audio cards: AdLib, DisneySource, and SoundBlaster.

</p>
    

