Here is the proudest moment of my Programming career: On April 17th, 2012 the Government of Ontario released the Mobile Website I worked on during 2011. 
<a href="https://www.services.gov.on.ca/wps/portal/so_pub/odr/odr">Beadonor.ca</a> is the official place to manifest your decision to be an organ donor. 
I am honored and privileged to have been able to contribute. <br/>
<br/>
You may wonder: If we already had the Organ Donor Card, why create a website and a database ? Because our survivors will always have the final world: Whatever
we sign or say while we are alive is not enough to allow organ donation.<br/>
<br/>
This website is a way to re-iterate our wish so it will more likely be respected by our families.
Hopefully with this additional medium of expression, more lives will be saved.
</p>
                                                            


<h2>Genesis</h2>
<p id="paperbox">
As a software engineer I am inclined to embrace technically challenging projects. But while Organ Donor Registration website is by far the simplest thing I ever programmed, 
it is also the most spiritually rewarding: To be a little gear in people's well-being system allowed me to work tirelessly and with great enthousiam.<br/>
<br/>
More satisfactory is to think of the genesis of this website: People from all over Ontario have elected representatives that in turn materialized 
their desire to help each other.<br/>
<br/>
Combined with a contribution to the <a href="http://www.aoda.ca/">Accessibility for Ontarians with Disabilities Act (AODA)</a>, it was 
two little steps for an engineer but two great steps for a nation :) !<br/>
<br/> 
</p>
      
   
