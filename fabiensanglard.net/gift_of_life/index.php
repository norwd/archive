   <h1>Gift Of Life</h1>
   <p id="paperbox">
	   	
          <a  href="Gift&#32;of&#32;Life&#32;logo_BL_3x2.jpg">
          <img src="gol.png" style="margin-left: 2ch;float:right; border:1px black solid;width:35%;">   
          </a> 
         <script type="text/javascript">
  var disqus_identifier = "gol" ;
