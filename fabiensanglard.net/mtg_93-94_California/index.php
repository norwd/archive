</div>
Having lived in Canada for ten years: Winter is cold, Winter is absence of light and Winter is death. The natural color to use was Black. 
The deck started as a MonoBlack but did poorly. I morphed it into a Deadguy Ale and it was more able to deal with the three other seasons.<br/>
