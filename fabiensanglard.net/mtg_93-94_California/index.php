Antiquities expansion  (released in March 1994) featured a card called Mishra Factory. The first land that could turn into a man is likely 
the best card in the Old School format. It also happen to feature four different artworks: Winter, Spring, Fall and Summer: One for each of the four seasons.<br/>
<br/>
From left to right: Spring, Fall, Winter and Summer<br/>
<br/>
<a href="photos/4_seasons_flat.jpg" data-lightbox="image-1" data-title="Four Seasons Decks">
<img class="screenshot" src="photos/4_seasons_flat.jpg"/><br/>
</a>
<br/>
I always loved the Mishra Factories and they gave me an idea: Since a lot of friends want to try Old School but don't have a deck for it, 
I would built a few for them to play.
Each deck would feature a full set of a season. Each deck would represent a significant archetype of the Old School format:<br/>
<br/>
<a href="photos/4_seasons.jpg" data-lightbox="image-1" data-title="Four Seasons Decks">
<img class="screenshot" src="photos/thumbs/4_seasons.jpg"/><br/>
</a>
<br/>
<a href="photos/4_seasons_inside.jpg" data-lightbox="image-1" data-title="Four Seasons Decks">
<img class="screenshot" src="photos/thumbs/4_seasons_inside.jpg"/><br/>
</a>
<br/> So far it has been really well received: I usually bring the four decks to a friend's place and I only ask to pick a season. 
It is pretty cool way to start an evening and feel like we are back to 1993.
<br/>


<h1>Winter</h1>
<div style="float:right; margin-left:20px;">
  <a href="photos/winter.png" data-lightbox="image-1" data-title="Four Seasons Decks">
<img src="photos/thumbs/winter_small.png"/><br/>
