<h1>Summer</h1>
<div style="float:right; margin-left:20px;">
<img  src="photos/thumbs/summer_small.png"  style="margin-bottom: 12px;"  />
</div> 
Summer is sun, Summer is Life and Summer is light. It is the opposite of Winter. It had to be a White Weenie. 
You can tell how much I like this deck since it is almost full black border (sadly the Black Lotus with is CE).<br/>
<br/>
This deck went through several iterations. I came in second place in Berkley with it:<br/>
<a href="photos/ww_in_the_making.jpg" data-lightbox="image-1" data-title="Four Seasons Decks">
<img class="screenshot" src="photos/thumbs/ww_in_the_making.jpg"/><br/>
</a>
<br/>
I eventually decided to cut Balance and Army of Allah. I also replaced the Strip Mine with an extra plain since I got mana screwed a few times.
The deck intentionally runs low on mana sources and rely on land tax.<br/>
The deck is played monotonically: Drop a lot of treats very fast, remove whatever is in the way with Swords and Disenchants. As soon as the 
board is advantageous: Wipe
all the lands with Armageddon.


