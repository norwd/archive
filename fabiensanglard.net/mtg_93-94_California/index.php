<h1>Fall</h1>
<div style="float:right; margin-left:20px;">

<a href="photos/fall.png" data-lightbox="image-1" data-title="Four Seasons Decks">
<img src="photos/thumbs/fall_small.png"/><br/>
</a>

</div>
Fall is red. The obvious deck to make was Burn. But to make it really competitive, representative of the format and also add a touch of nostalgia it had
to be a Lestree Zoo Deck. The list is not what Bertrand played originally during the first World Championship 
(<a href="https://fabiensanglard.net/mtg_93-94_Toronto/event1.php">and what we replayed in Toronto</a>). It has been modified after much playtesting.<br/>
<br/>
The Philosophy of this deck is twofolds:
<ul>
  <li>Drop a creature turn one or two. Do 4-6 damage.</li>
  <li>Finish your opponent with Lightning Bolt, Chain Lightning and Psionic Blast</li>
</ul>
While the Serendib Efreets and the Gorillas are no brainers I still question if I should have an other Ernahm Djinn instead of the Elvish Archers. 
The Whirling Devish can look bizarre but they are very good against Juzamns and can grow pretty fast against "Spring" deck.
<br/>
This desk looks strong but it has one huge weakness, called 
<!--
<a href="photos/bottle.jpg" data-lightbox="image-1" data-title="Four Seasons Decks">
<img src="photos/thumbs/bottle.jpg"/><br/>
</a>-->
City in a bottle which can  wipe out the entire first row. If the bottle doesn't come too early during the game the deck can still do well since it usually finishes 
with Burns.<br/>
