<h2>The Future Was Here: The Commodore Amiga</h2>
<p>
  <a href="http://www.amazon.ca/The-Future-Was-Here-Commodore/dp/0262017202">
The Future Was Here: The Commodore Amiga</a> takes the most iconic applications of the Amiga in order to progressively explain the overall architecture :<br/>
<br/>
<img src="amiga_architecture.png" style="display:block; margin-left:auto; margin-right:auto; width:603px;height:369px;"/>
<div style="display:block; text-align:center; width:100%; font-size:92%;"><u>The Amiga 1000 hardware architecture</u></div>
<br/>
 <br/>
 <img src="boing.png" style="float:left; margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/>
Chapter one is all about the legendary <a href="http://www.youtube.com/watch?v=qwFbWkr71wA&t=129">Boing</a> demo. It explains :
<ul style="overflow: auto;">
  <li>The Bitplanes based Display system.</li>
  <li>Denise ViewPort.</li>
  <li>Denise capability to set bitplane offset individually.</li>
  <li>Paula Sound chip.</li>
</ul> 
<u><b>Bonus :</b></u> Check out the <a href="http://www.youtube.com/watch?v=cJcO628yCcU">Making-off video</a>.
<div style="clear:both;"></div>
  <br/>
  <img src="deluxePaint.png" style="float:right; margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/>
The book then switch to Deluxe Paint internals and  demonstrates :
<ul style="overflow: auto;">
   <li>The different graphic modes available (including EHB).</li>
   <li>Agnus DMA Blittering.</li>
   <li>Agnus Copper chip.</li>
   <li>Difference between Fast RAM / Chip RAM.</li>
   
</ul>
<div style="clear:both;"></div>
</p>

<img src="juggler.gif" style="float:left; margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/>
Next is the Juggler which is a base to describe :
<ul style="overflow: auto;">
   <li>The Amiga HAM graphic mode allowing 4096 colors on screen: A late hack that proved paramount for raytracers and digital photography.</li>
 </ul>
<p>
   I won't spoil the rest but the list goes on an on, including the genlock so instrumental to the <a href="http://www.youtube.com/watch?v=GTb6zX7Hd5I">rotoscoping used in Another World</a>, AmigaOS, The DemoScene (with a strong emphasis on the beloved Copper) and an in-depth analysis of a Shmup :
   <a href="http://www.youtube.com/watch?v=eXBpMCVicic">Menace</a> that featured hardware collision detection.<br/>
<br/>
The final chapter "The way the future Was" takes an interesting point of view on the end of the Amiga. Instead of the usual Commodore blaming, the author points towards the innovative design of the machine itself and its inability to run...DOOM.
  <div style="clear:both;"></div>
</p>


<br>
<h2>Racing the Beam</h2>
<p>
  If you think programming those days is hard, think of the Atari VCS architecture: 128 bytes of RAM, 
  one color per sprite scanline and NO framebuffer (you had to pace your renderer to the TV electron beam).<br/>
  <br/>
  <img src="atari_architecture.png" style="display:block; margin-left:auto; margin-right:auto;width:460x;height:407px;"/>
  <div style="display:block; text-align:center; width:100%; font-size:92%;"><u>The Atari 2600 hardware architecture</u></div>
  <br/>
  <a href="http://www.amazon.com/Racing-Beam-Computer-Platform-Studies/dp/026201257X/ref=sr_1_1?ie=UTF8&qid=1381099490&sr=8-1&keywords=racing+the+beam">Racing the Beam: The Atari Video Computer System</a> is a great book which explains the Atari 2600 hardware via six games deconstructions: 
  <ul>
    <li><a href="http://www.youtube.com/watch?v=3m86ftny1uY">Combat</a></li>
    <li><a href="http://www.youtube.com/watch?v=sdZahXk-e9Y">Adventure</a></li>
    <li><a href="http://www.youtube.com/watch?v=HL2p2ANFlQ4">Pac-Man</a></li>
    <li><a href="http://www.youtube.com/watch?v=pvjajVf3BEc">Yar's Revenge</a></li>
    <li><a href="http://www.youtube.com/watch?v=MhXMYw1lXY0">PitFall!</a></li>
    <li><a href="http://www.youtube.com/watch?v=6SkWIPKFrxk">Star-Wars: The empire Strikes Back</a></li>
  </ul>
  The many tricks and the backstories of the machine and games are mesmerizing.<br/>
  <br/>
  As a Bonus, read Landon Dyer (Dadhacker)'s blog about the post 2600 story of Atari: <a href="http://www.dadhacker.com/blog/?p=995">The Atari ST (part 1)</a>, <a href="http://www.dadhacker.com/blog/?p=1000">The Atari ST (part 2)</a> and <a href="http://www.dadhacker.com/blog/?p=987">Donkey Kong and Me</a>.
</p>

<br>
<h2>Codename Revolution: The Nintendo Wii Platform</h2>
<p>
  Not very technical (although it details the Wii Remote greatly), it translates well the deliberated "Power isn't everything" of the Wii.
</p>



<h2>Commodore: A Company on the Edge</h2>
<p>
  Note focused on the technical side either but featuring the key events of the history of Commodore, from MOS Technology to The Fall in 1992-1994.
  A great follow up if you enjoyed "The Future was Here".
</p>

<h2>Commodore: The Amiga years</h2>
<p>
  A book dedicated to the Amiga that unfortunately keep on being delayed :( ! Let's keep on hoping.
</p>

