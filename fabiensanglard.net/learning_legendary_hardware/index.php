If you love software, you also try to understand the hardware to the deep down. It is not an 
easy task : There are many great programming books but few that explains hardware very well (as Michael Abrash <a href="http://www.phatcode.net/res/224/files/html/ch13/13-01.html">once wrote</a>: the Intel Documentation is "slightly more fun to read than the phone book"). In my quest
for knowledge, I found two hardware books to be outstanding:
<ul>
  <li><a href="http://www.amazon.com/Code-Language-Computer-Hardware-Software/dp/0735611319/ref=sr_1_1?ie=UTF8&qid=1358293457&sr=8-1&keywords=code+hidden+language">Code: The Hidden Language of Computer Hardware and Software</a>.</li>
  <li><a href="http://www.amazon.com/Computer-Organization-Design-Fourth-Architecture/dp/0123747503/ref=sr_1_1?s=books&ie=UTF8&qid=1358293499&sr=1-1&keywords=computer+organization+and+design">Computer Organization and Design</a>.</li>
  
</ul>

  Those books are good for "current" hardware but there is a lot to learn from previous generations. The cult machine <a href="http://spectrum.ieee.org/computing/hardware/amiga-the-computer-that-wouldnt-die">
That Wouldn't Die</a>, the Amiga with its elegant co-processor architecture is a vivid example:<br/>
<div style="clear:both;"></div>
<img src="amiga_architecture.png" style="margin-top:20px;display:block; margin-left:auto; margin-right:auto;"/><br/>
<br/>
Some parts were so good that Future Crew devteam wrote an <a href="../second_reality/second_reality_dis.php">Amiga Copper emulator for PC</a> in order to produce the legendary <a href="../second_reality/index.php">Second Reality</a>. But ultimately, its greatest asset (a small CPU with dedicated co-processors) became its greatest liability when it proved "uniquely unsuited to run DOOM".<br/>
<br/>
Older hardware are much simpler (the Atari 2600 did not even have a framebuffer). That simplicity lowers the learning curve and is less likely to discourage beginners. I consider them a good starting point before learning superScalared-cachelined-SMPed-MESIed-systems.<br/>
<br/>
A few dedicated engineers have documented those legendary machines by de-constructing the greatest applications they used to run.
<a href="http://mitpress.mit.edu/books/series/platform-studies">MIT Press</a> book editor has opened 
a <a href="http://platformstudies.com/examples.html">Platform Studies</a> section for them and they are incredibly pleasant to read:<br/>
<br/>
<br/>
<div style="width:100%;display:block;text-align:center;">
 
  <a href="http://www.amazon.com/The-Future-Was-Here-Commodore/dp/0262017202/ref=sr_1_1?ie=UTF8&qid=1381099473&sr=8-1&keywords=the+future+was+here"><img src="future-was-here-the-commodore-amiga.jpg" style="margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/></a>
  <a href="http://www.amazon.com/Racing-Beam-Computer-Platform-Studies/dp/026201257X/ref=sr_1_1?ie=UTF8&qid=1381099490&sr=8-1&keywords=racing+the+beam"><img src="9780262012577.jpg" style="margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/></a>
  <a href="http://www.amazon.com/Commodore-Company-Edge-Brian-Bagnall/dp/0973864966/ref=sr_1_1?ie=UTF8&qid=1381099504&sr=8-1&keywords=commodore+on+the+edge"><img src="on_the_edge.jpg" style="margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/></a>
  <a href="http://www.amazon.com/Commodore-Amiga-Years-Brian-Bagnall/dp/0973864990/ref=sr_1_1?ie=UTF8&qid=1381099516&sr=8-1&keywords=commodore+the+amiga+years"><img src="commodore.png" style="margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/></a>
  <a href="http://www.amazon.com/Codename-Revolution-Nintendo-Platform-Studies/dp/026201680X/ref=sr_1_1?ie=UTF8&qid=1381099458&sr=8-1&keywords=codename+revolution"><img src="9780262016803.jpg" style="margin-right: 25px;box-shadow: rgb(119, 119, 119) 3px 3px 3px;"/></a><br/>
  <br/>
</div>
