<h2>About Mathematics</h2>
<p id="paperbox">
	If the importance of good illustrations is obvious, here is more insight about the mathematic metric:
	I first noticed it while reading <a href="http://www.amazon.com/Real-Time-Shadows-Elmar-Eisemann/dp/1568814380/ref=sr_1_1?ie=UTF8&qid=1391105083&sr=8-1&keywords=real+time+shadow">Real-time Shadows</a>.
	Near the beginning of the ouvrage, readers would come across the following line:<br/>
	<pre class="long">


		(p,q) := { r | r := p + α ( q - p) , 0 < α < 1 }    *

	</pre>
	<p>
		The author must have felt bad about it because they included a footnote :<br/>

	<pre class="long">


		* This says that the segment (p,q) consists of all points r that between p and q.

	</pre>
	<p>
		<br/>
		The footnote was raising the question: When should we describe something with mathematics instead of words ?
		When is is better for the reader ? There is no absolute answer because some people prefer the mathematic form while others prefer the English
		form. <br/>
		<br/>
		<u><b>Trivia :</b></u> <a id="rts" href="real-time-shadows.jpg">Here is the page</a> in question just for reference.<br/>
		<br/>
		Over the years I have found that there are two kinds of programmers :
		<ul>
			<li>Those that can use mathematics notation.</li>
			<li>Those that are confused and furthermore discouraged by mathematic notation.</li>
		</ul>
	<p>
		I used to attribute dislike of mathematics to laziness but I changed my mind: Some people have different brains and respond
		differently to "mathematicness". Try to learn yourself and acknowledge how your brain likes to have things explained.
