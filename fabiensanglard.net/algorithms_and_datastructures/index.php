<p>
	Over the years running this moderately popular website, I have been asked many times what is the best book
	about Algorithms and Data Structures. The answer is always "It depends" !<br/>
	<br/>
	It depends on how the programmer's brain works and what kind of notation he is comfortable with. 
	There are many flavors of mindset out there but it usually comes down to two metrics :
	<ul>
	    <li>Mathematics</li>
	    <li>Illustrations</li>
	</ul>
	<p> 
	Here are five good books that rank differently on those metrics and will fit different preferences :<br/>
	<ul>
		<li><a href="http://www.amazon.com/Algorithm-Design-Manual-Steven-Skiena/dp/1848000693/ref=sr_1_1?s=books&ie=UTF8&qid=1392328703&sr=1-1&keywords=algorithms+manual">The Algorithm Design Manual</a> by Steven S Skiena.</li>
		<li><a href="http://www.amazon.com/Computer-Programming-Volumes-1-4A-Boxed/dp/0321751043/ref=sr_1_1?s=books&ie=UTF8&qid=1392328799&sr=1-1&keywords=the+art+of+computer+programming#selectedObb=rbb_rbb_trigger">The Art of Computer Programming</a> by Donald E. Knuth.</li>
		<li><a href="http://www.amazon.com/Programming-Interviews-Exposed-Secrets-Landing/dp/1118261364">Programming Interviews Exposed</a> by John Mongan, Noah Kindler and Eric Giguere.</li>
		<li><a href="http://www.amazon.com/Algorithms-4th-Robert-Sedgewick/dp/032157351X/ref=sr_1_1?s=books&ie=UTF8&qid=1392328641&sr=1-1&keywords=algorithm">Algorithms</a> by Robert Sedgewick and  Kevin Wayne.</li>
		
		<li><a href="http://www.amazon.com/Introduction-Algorithms-Thomas-H-Cormen/dp/0262033844/ref=sr_1_1?s=books&ie=UTF8&qid=1392328730&sr=1-1&keywords=introduction+to+algorithms">Introduction to Algorithms</a> by Thomas H. Cormen, Charles E. Leiserson, Ronald L. Rivest and Clifford Stein.</li>
		
	</ul>
